#!/usr/bin/env bash
# strace_examples.sh — примеры трассировки системных вызовов
#
# Что показывает strace:
#   strace работает в userspace и перехватывает системные вызовы процесса.
#   Он не видит напрямую, что происходит внутри ядра на уровне функций,
#   но прекрасно показывает, какие syscalls вызывает наша тестовая утилита:
#   open/openat, ioctl, write, close и т.д.
#
# Для этого проекта strace особенно полезен, чтобы увидеть:
#   - как userspace открывает /dev/lkm_debug;
#   - как вызывается ioctl();
#   - как передаётся write();
#   - сколько времени занимает syscall с точки зрения userspace.
#
# Предварительные условия:
#   1) модуль уже загружен: sudo insmod ../lkm_debug.ko
#   2) userspace-утилита собрана: cd ../userspace && make

set -euo pipefail
TOOL="../userspace/ioctl_test"

echo "=== 1. Basic strace: все системные вызовы ==="
#
# Здесь мы трассируем запуск команды ping и печатаем хвост вывода,
# чтобы не засорять терминал слишком большим логом.
#
# Полезно для первого знакомства: видно openat/ioctl/close и прочие вызовы.
strace -f ${TOOL} ping 2>&1 | tail -20

echo
echo "=== 2. strace: только ioctl + open + close ==="
#
# -e trace=... фильтрует только интересующие системные вызовы.
# Это удобнее, когда нужно сфокусироваться именно на взаимодействии
# с устройством, а не на всём служебном шуме процесса.
strace -e trace=ioctl,open,close,openat ${TOOL} ping 2>&1

echo
echo "=== 3. strace: частота системных вызовов ==="
#
# -c показывает сводную статистику по syscall'ам:
#   - сколько раз вызван каждый syscall;
#   - сколько времени суммарно потрачено;
#   - долю времени.
#
# Это помогает быстро понять, куда уходит время на userspace-стороне.
strace -c ${TOOL} slow 2>&1

echo
echo "=== 4. strace: временные метки и длительность ==="
#
# -tt добавляет точное время начала syscall,
# -T  показывает длительность syscall.
#
# Удобно, если хочется сравнить, насколько медленнее IOCTL_SLOW по сравнению
# с IOCTL_PING, с точки зрения пользователя.
strace -T -tt ${TOOL} ping 2>&1

echo
echo "=== 5. strace: attach к уже работающему процессу ==="
#
# Иногда полезно не запускать процесс под strace с нуля, а подключиться к уже
# работающему PID. Это типичный сценарий для отладки демонов и long-running
# процессов, которые взаимодействуют с устройством.
#
# Пример:
#   strace -p <PID>
#
# Для этой демо-утилиты сценарий attach не так важен, но как практика
# он очень полезен.
echo "  (run: strace -p <PID>)"
echo "  Useful to attach to a long-running user daemon that uses the device"
